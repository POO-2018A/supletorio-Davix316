    public void llenarTablaRegistro(){
        if(registro.size() > 0){
            Articulos articulos = new Articulos();
            DefaultTableModel model = (DefaultTableModel) tblRegistro.getModel();
            for(int i = 0; i < registro.size(); i ++){
                articulos = (Articulos) registro.get(i);
                Object articulosG [] = {articulos.getNombre(),articulos.getPrecio(),articulos.getMarca(),articulos.getTipo()};
                model.insertRow(i, articulosG);
            }
        }
    }
