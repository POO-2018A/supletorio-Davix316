    private void btnOkCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkCActionPerformed
        // TODO add your handling code here:
        Computador compu = new Computador();
        
        String tipo= cmbTipoC.getItemAt(cmbTipoC.getSelectedIndex());
        int capacidad= Integer.parseInt(txtCapacidadC.getText());
        int ram= Integer.parseInt(txtRamC.getText());
        String procesador = txtProcesadorC.getText();
               
        if(tipo.equals("")|| procesador.equals("")){
            JOptionPane.showMessageDialog(rootPane, "Llene todos los campos");
           
        }else{
            compu = new Computador (tipo,capacidad,ram,procesador);
            btnRegistrar.setEnabled(true);
        }
    }//GEN-LAST:event_btnOkCActionPerformed

